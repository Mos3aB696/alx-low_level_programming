#include <stdio.h>
/**
 * add - add two numbers
 * @a: first arg
 * @b: first arg
 *
 * Return: a + b
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - substraction two numbers
 * @a: first arg
 * @b: first arg
 *
 * Return: a - b
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - multiplication two numbers
 * @a: first arg
 * @b: first arg
 *
 * Return: a + b
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - division two numbers
 * @a: first arg
 * @b: first arg
 *
 * Return: a / b
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - moduols two numbers
 * @a: first arg
 * @b: first arg
 *
 * Return: a % b
 */
int mod(int a, int b)
{
	return (a % b);
}
